#include <stdio.h>
#include <string.h>

int binom(int n, int k) {
    if (k > n) return 0;
    if (k == 0) return 1;
    return binom(n-1, k) + binom(n-1, k-1);
}

//# Chuản bị bảng tra
int look_up[37][19];

void init() {
    memset(look_up, 0, sizeof(look_up));
}

int binom2(int n, int k){
    if (k > n) return 0;
    if (k == 0) return 1;
    if (look_up[n][k]) return look_up[n][k];
    look_up[n][k] = binom2(n - 1, k) + binom2(n - 1, k - 1);
    return look_up[n][k];
}

int main() {
    int m;
    scanf("%d", &m);
    for (int n = 1; n <= m; ++n){
        for (int k = 0; k <= n; ++k)
            printf("%d ", binom(n, k));
        printf("\n");
    }
    for (int n = 1; n <= m; ++n){
        for (int k = 0; k <= n; ++k)
            printf("%d ", binom2(n, k));
        printf("\n");
    }
    return 0;
}