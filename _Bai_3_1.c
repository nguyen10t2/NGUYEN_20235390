#include <stdio.h>

int lucas(int n) {
    if (n == 0) return 2;
    if (n == 1) return 1;
    return lucas(n - 1) + lucas(n - 2);
}

int main() {
    int n_dtn;
    scanf("%d", &n_dtn);
    printf("%d\n", lucas(n_dtn));
    return 0;
}