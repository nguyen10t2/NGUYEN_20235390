#include <iostream>

void rotate(int& a_90, int& b_90, int& c_90) {
    int temp_90 = a_90;
    a_90 = b_90;
    b_90 = c_90;
    c_90 = temp_90;
}

int main() {
    int a_90, b_90, c_90;
    std::cin >> a_90 >> b_90 >> c_90;
    // In ra giá trị trước khi xoay
    std::cout << "Before rotation: " << a_90 << " " << b_90 << " " << c_90 << std::endl;
    // Gọi hàm rotate với 3 tham số a_90, b_90 và c_90
    rotate(a_90, b_90, c_90);
    // In ra giá trị sau khi xoay
    std::cout << "After rotation: " << a_90 << " " << b_90 << " " << c_90 << std::endl;
    // -- Dao Trong Nguyen _ 20235390 _ IT2-03 _ IT3040 _ 750829_ 2024.2 --
    return 0;
}